
function openNav() {
    document.getElementById("mySidenav").style.width = "200px";
    document.getElementById("mainBox").style.marginRight = "200px";
    document.body.style.backgroundColor = " rgb(85, 132, 225)";
  }
  
  function closeNav() {
    document.getElementById("mySidenav").style.width = "0";
    document.getElementById("mainBox").style.marginRight= "0";
    document.body.style.backgroundColor = "#DDF2FD";
  }